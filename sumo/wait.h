/*-----------------------------------------*/
/*短いウエートルーチン                       */
/*-----------------------------------------*/
void wait1(int t)
{
	int i;
	for(i=0;i<=t;i++);
	return;
}
/*---------------------------------------*/
/*長いウエートルチーン                    */
/*--------------------------------------*/
void wait10(int t)
{
	int i;
	for(i=0;i<=t;i++){wait1(3070);}
	return;
}
